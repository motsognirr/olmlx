class TestMinPivot:
    def test_select_pivot_respects_min_pivot(self):
        ids = mx.array([[5] * 100])
        for _ in range(20):
            p = _select_pivot(ids, 0, 8, min_pivot=40)
            assert p is not None and 40 <= p <= 100 - 8 - 1

    def test_select_pivot_returns_none_when_no_room(self):
        ids = mx.array([[5] * 30])
        assert _select_pivot(ids, 0, 8, min_pivot=25) is None

    def test_select_pivots_respects_min_pivot(self):
        ids = mx.array([[5] * 200])
        for _ in range(10):
            ps = _select_pivots(ids, 0, 8, 4, min_pivot=90)
            assert ps is not None
            assert all(p >= 90 for p in ps)

    def test_legacy_behavior_unchanged_without_min_pivot(self):
        import random

        ids = mx.array([[5] * 100])
        random.seed(42)
        a = _select_pivot(ids, 0, 8)
        random.seed(42)
        b = _select_pivot(ids, 0, 8, min_pivot=0)
        assert a == b


