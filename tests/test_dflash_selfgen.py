class TestCycleTrainingBatches:
    def _seqs(self):
        # (sequence, prompt_len) of assorted lengths
        return [
            (list(range(2, 42)), 5),
            (list(range(2, 44)), 7),
            (list(range(2, 46)), 6),
            (list(range(2, 48)), 4),
            (list(range(2, 80)), 9),
            (list(range(2, 82)), 8),
        ]

    def test_yields_padded_batches_with_pivot_lo(self):
        it = cycle_training_batches(
            self._seqs(), batch_size=2, pad_token_id=0, min_len=10
        )
        ids, pivot_lo = next(it)
        assert ids.shape[0] == 2
        # pivot_lo is the max prompt length within the batch
        assert isinstance(pivot_lo, int) and pivot_lo >= 4
        # rows padded to the in-batch max with pad_token_id
        assert ids.shape[1] == max(int((ids[r] != 0).sum().item()) for r in range(2))

    def test_filters_short_sequences(self):
        seqs = [(list(range(2, 8)), 2)] + self._seqs()
        it = cycle_training_batches(seqs, batch_size=2, pad_token_id=0, min_len=10)
        for _ in range(6):
            ids, _ = next(it)
            assert ids.shape[1] >= 10

    def test_cycles_forever(self):
        it = cycle_training_batches(
            self._seqs(), batch_size=2, pad_token_id=0, min_len=10
        )
        got = [next(it) for _ in range(10)]  # > 3 batches/epoch
        assert len(got) == 10


