class TestPrepareSelfGenerate:
    def test_pivot_lo_tuple_batches_thread_min_pivot(self, tmp_path, monkeypatch):
        from olmlx.engine.dflash import prepare as prepare_mod

        _write_target_config(tmp_path, vocab_size=64, hidden_size=16)
        seen: list[int] = []
        orig = prepare_mod._select_pivots

        def spy(input_ids, pad, bs, k, min_pivot=0):
            seen.append(min_pivot)
            return orig(input_ids, pad, bs, k, min_pivot=min_pivot)

        monkeypatch.setattr(prepare_mod, "_select_pivots", spy)

        def batches():
            while True:
                ids = (mx.arange(64, dtype=mx.int32)[None, :] % 62) + 2
                yield (mx.concatenate([ids] * 2, axis=0), 12)

        prepare_dflash_draft(
            model_path=tmp_path,
            steps=2,
            batch_size=2,
            seq_len=64,
            block_size=4,
            num_hidden_layers=1,
            num_target_layers=2,
            output_dir=tmp_path / "out",
            _target_loader=_mock_target_loader(64, 16, 4),
            _batch_iterator=batches(),
            train_windows_per_step=2,
        )
        assert seen and all(m == 12 for m in seen)

    def test_self_generate_end_to_end(self, tmp_path):
        _write_target_config(tmp_path, vocab_size=64, hidden_size=16)
        prompts = [[(7 * i + j) % 60 + 2 for j in range(6)] for i in range(8)]
        out = prepare_dflash_draft(
            model_path=tmp_path,
            steps=2,
            batch_size=2,
            seq_len=64,
            block_size=4,
            num_hidden_layers=1,
            num_target_layers=2,
            output_dir=tmp_path / "out",
            _target_loader=_mock_target_loader(64, 16, 4),
            self_generate=True,
            selfgen_num_seqs=8,
            selfgen_max_new=24,
            _prompt_iter=iter(prompts),
        )
        assert (out / "config.json").exists()
        cfg = json.loads((out / "config.json").read_text())
        assert cfg["dflash_config"]["mask_token_id"] == 0  # pad id

    def test_self_generate_rejects_precomputed(self, tmp_path):
        _write_target_config(tmp_path, vocab_size=64, hidden_size=16)
        with pytest.raises(ValueError, match="self_generate.*use_precomputed"):
            prepare_dflash_draft(
                model_path=tmp_path,
                steps=1,
                self_generate=True,
                use_precomputed=tmp_path / "shards",
                _target_loader=_mock_target_loader(64, 16, 4),
            )
